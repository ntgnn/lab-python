# Atribuição simples
a = 3

# Substituição de valor
a = a + 7
print(a)

# Atribuição aditiva, equivale a = a + 5
a += 5
print(a)

# Atribuição subtrativa, equivale a = a - 3
a -= 3
print(a)

# Atribição multiplicativa, equivale a = a * 2
a *= 2
print(a)

# Atribuição divisiva, equivale a = a / 4
a /= 4
print(a)

# Atribuição modulativa, equivale a = a % 4
a %= 4
print(a)

# Atribuição exponenciativa, equivale a = a ** 8
a **= 8
print(a)

# Atribuição divisiva de inteiros, equivale a = a // 256
a //= 256
print(a)

